public class Exer6 {
    public static void main(String[] args) {
        byte meuByte=10;

        //byte para short
        short meuShort=meuByte;
        System.out.println(meuShort);

        //short para int
        int meuInt = meuShort;
        System.out.println(meuInt);

        //int para float
        float meuFloat=meuInt;
        System.out.println(meuFloat);

        //float para double
        double meuDouble = meuFloat;
        System.out.println(meuDouble);

        //maior nao cabe no menor.
        //byte v1=128;
        //meuByte = meuInt;
    }
}
